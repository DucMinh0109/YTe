import requests
from bs4 import BeautifulSoup
from docx import Document

def crawl_paragraphs_and_list_items(url, doc):
    # Gửi yêu cầu HTTP đến trang web
    response = requests.get(url)

    if response.status_code == 200:
        # Phân tích cú pháp HTML với BeautifulSoup
        soup = BeautifulSoup(response.text, 'html.parser')

        # Tìm tất cả các thẻ <p> và <li> trong trang
        elements = soup.find_all(['p', 'li'])

        # Tìm vị trí của thẻ <p> có nội dung "Nội dung bài viết"
        start_index = -1
        end_index = -1
        for i, element in enumerate(elements):
            text = element.get_text(strip=True)
            if text == "Nội dung bài viết":
                start_index = i
            if text == "Đặt khám tiện lợi cùng YouMed":
                end_index = i

        # Nếu không tìm thấy thẻ "Nội dung bài viết" hoặc "Đặt khám tiện lợi cùng YouMed"
        if start_index == -1 or end_index == -1:
            print("Không tìm thấy các thẻ yêu cầu.")
            return

        # In các thẻ nằm giữa start_index và end_index (bao gồm cả start_index và không bao gồm end_index)
        for element in elements[start_index:end_index]:
            text = element.get_text(strip=True)
            # Thêm nội dung vào file Word
            print(text)
            doc.add_paragraph(text)

    else:
        print(f"Failed to retrieve the page. Status code: {response.status_code}")


def save_to_word(urls):
    # Tạo một tài liệu Word mới
    doc = Document()
    doc.add_heading('Crawled Content', 0)

    # Duyệt qua tất cả các URL và crawl dữ liệu vào file Word
    for url in urls:
        doc.add_heading(f"Crawled from {url}", level=1)
        crawl_paragraphs_and_list_items(url, doc)

    doc.save('disease_crawled.docx')
    print("Dữ liệu đã được lưu vào file 'disease_crawled.docx'.")


